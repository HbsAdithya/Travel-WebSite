			<h1>Dambulla Golden Buddha and the Cave Temple</h1>
			
			<p>Dambulla is a large Town, situated in the Matale District, Central Province of Sri Lanka,situated 148km north-east of colombo and 72km 
			north of kandy. Due to its the centre of vegetable distribution in the country Major attraction of the area includes the largest and best
			preserved cave temple complex of sri lanka and the Rangiri Dambulla International Stadium, famous for being built in just 167 days. The area 
			also boasts the largest rose quartz mountain range in South Asia, and the Iron wood forest,or Na uyana Aranya.</p>

			<p>It is the largest and best preserved cave temple complex in Sri Lanka. The rock towers 160m over the surrounding plains. 
			There are more than 80 documented caves in the surrounding, Major attractions are spread over 5 caves, which contain statues and paintings.
			This paintings and statues are related to Lord Buddha and his life. There are a total of 153 Buddha statues ,3 statues of 
			Sri lankan kings and 4 statues of god and goddess. The latter 4 include 2 statues pf hindu gods,Vishnu and Ganesh. The murals cover 
			an area of 2100m2. Depictions in the walls of the caves include Buddha’s temptation by demon Mara and Buddha’s first sermon.</p>
			
		
	
